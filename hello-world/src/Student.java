public class Student {
 public String name;
 public int yearGraduation;
 public double media;

 public Student(String name, int yearGraduation, double media) {
    this.name = name;
    this.yearGraduation = yearGraduation;
    this.media = media;
 }

 public void setYearGraduation(int yearGraduation) {
    this.yearGraduation = yearGraduation;
 }

}
